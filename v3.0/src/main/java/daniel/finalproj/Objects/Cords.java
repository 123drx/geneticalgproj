package daniel.finalproj.Objects;

public class Cords {
    int day;
    int hour;
    public Cords(int day, int hour)
    {
        this.day =day;
        this.hour = hour;
    }
    public Cords()
    {
        
    }
    public int getDay() {
        return day;
    }
    public void setDay(int day) {
        this.day = day;
    }
    public int getHour() {
        return hour;
    }
    public void setHour(int hour) {
        this.hour = hour;
    }
    
}
